/**
 * CreatureType.java
 *
 * This file defines the CreatureType enum, which represents different types of creatures
 * in the game. These types can be used to determine movement in the game.
 * For Example: creature on a square is compared to the creature on the dragonCard.
 *
 *
 * Author: Alex Ung
 * Date: 29/04/2024
 */

package src.board;

/**
 * Enumeration of creature types available in the game.
 */
public enum CreatureType {
    /**
     * Represents a baby dragon.
     */
    BABY_DRAGON,

    /**
     * Represents a bat.
     */
    BAT,

    /**
     * Represents a spider.
     */
    SPIDER,

    /**
     * Represents a salamander.
     */
    SALAMANDER,

    /**
     * Represents a pirate dragon.
     */
    PIRATE_DRAGON
}
